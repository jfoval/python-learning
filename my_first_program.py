# My First Python Program!
# Lines starting with # are comments - Python ignores them

# Print messages to the screen
print("Hello, World!")
print("Python is awesome!")
print("")  # This prints a blank line

# Variables - like labeled boxes that store information
name = "John"
age = 37

# Print variables
print("My name is", name)
print("I am", age, "years old")

# Do some math
print("In 5 years, I'll be", age + 5)
print("I just learned to code!")
print("10 times my age is", age * 10)

# Let's get interactive - ask the user a question!
user_name = input("What's your name? ")
print("Nice to meet you,", user_name + "!")
